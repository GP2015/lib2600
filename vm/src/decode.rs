macro_rules! create_sel {
    ($enum_name:ident, $(($var_name:ident, $($bit_match:literal),+ $(,)?)),+ $(,)?) => {
        #[derive(Clone, Copy, Debug)]
        pub enum $enum_name {
            $($var_name,)+
        }

        impl $enum_name{
            pub fn from_opcode(opcode: u8) -> Self{
                match opcode{
                    $($($bit_match => Self::$var_name,)+)+
                    _ => panic!("uncovered opcode {:02X}", opcode),
                }
            }
        }
    };
}

